package bus;

public class Bus {
	String school;
	String hwan;
	String six;

	String times, lotari, HGU, gokgang, heungHae;
	
	String version, siz;
	
	String tZone, timesplit;

	public Bus() {	}

	public String getSchool() {return school;}
	public void setSchool(String school) {this.school = school;}

	public String getHwan() {return hwan;}
	public void setHwan(String hwan) {this.hwan = hwan;}
	
	public String getSix(){return six;}
	public void setSix(String six){this.six = six;}
	
	
	public String getTimes(){return times;}
	public void setTimes(String times){this.times = times;}
	
	public String getLotari(){return lotari;}
	public void setLotari(String lotari){this.lotari = lotari;}
	
	public String getHGU(){return HGU;}
	public void setHGU(String HGU){this.HGU = HGU;}
	
	public String getGokgang(){return gokgang;}
	public void setGokgang(String gokgang){this.gokgang = gokgang;}
	
	public String getHeungHae(){return heungHae;}
	public void setHeunHae(String heungHae){this.heungHae = heungHae;}
	
	
	public String getVesion(){return version;}
	public void setVersion(String version){this.version = version;}
	
	public String getSiz(){return siz;}
	public void setSiz(String siz){this.siz = siz;}
	
	
	public String getTZone(){return tZone;}
	public void setTZone(String tZone){this.tZone=tZone;}
	
	public String getTimeSplit(){return timesplit;}
	public void setTimeSplit(String timesplit){this.timesplit = timesplit;}
}